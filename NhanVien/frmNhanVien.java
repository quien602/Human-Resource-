    private void jbAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddActionPerformed
        String MaNV  = jtfMaNV.getText();
        String HoTen = jtfHoTen.getText();
        String CCCD = jtfCCCD.getText();
        String NgaySinh = jtfngaysinh.getText();
        String QueQuan = jtfQueQuan.getText();
        boolean GioiTinh = jrbsex.isSelected();
        // String ngaysinh = jdate.getDateFormatString();
        // String strPattern = "[^a-z ]";
        String SdtNV = jtfDienThoai.getText();
        String MaPB = listPB.get(jcbMaPB.getSelectedIndex()).getMaPB();
        String MaCV = listCV.get(jcbMaCV.getSelectedIndex()).getMaCV();
        String MaKT = listKT.get(jcbMaKT.getSelectedIndex()).getMaKT();

        Kiemtra c = new Kiemtra();
        if (!checkinfo()) {
            return;
        }


        Date d = null;
        if (NgaySinh == null || NgaySinh.equals("") || !NgaySinh.matches(regexDDMMYYYY)) {
            JOptionPane.showMessageDialog(this, "Lỗi ngày sinh", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            // JOptionPane.showMessageDialog(null, "Lỗi ngày sinh");
            jtfngaysinh.setText("");
            jtfngaysinh.requestFocus();
            return;
        } else {
            try {
                d = new SimpleDateFormat("dd/MM/yyyy").parse(NgaySinh);
            } catch (ParseException ex) {
                Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                return;
            }
        }
        NhanVien nhanVien = new NhanVien(MaNV, HoTen, CCCD,d,QueQuan, GioiTinh, SdtNV, MaPB,MaCV,MaKT);
        ArrayList<NhanVien> listCheck = new NhanVienDAO().CheckID(MaNV);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng. Vui lòng kiểm tra lại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            jtfMaNV.setText("");
            jtfMaNV.requestFocus();
        } else {

            NhanVien insert = new NhanVienDAO().addNew(nhanVien);

            if (insert != null) {

                showAll();
            }
        }

//        else{
//        JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
//        }
    }//GEN-LAST:event_jbAddActionPerformed

