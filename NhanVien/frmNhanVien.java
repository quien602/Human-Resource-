    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);

        }
        int selectedIndex = jcbMaPB.getSelectedIndex();
        Phongban getPhongban = listPB.get(selectedIndex);
        ArrayList<NhanVien> nvs = new NhanVienDAO().findByIDPhongban(getPhongban.getMaPB());
            //ArrayList<SinhVien> sinhViens = new SinhVienDAO().CheckID(g)
            for (NhanVien nv : nvs) {
                Vector vector = new Vector();
                vector.add(nv.getMaNV());
                vector.add(nv.getHoTen());
                vector.add(nv.getCCCD());
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                vector.add(dateFormat.format(nv.getNgaySinh()));
                vector.add(nv.getQueQuan());
                // vector.add(sv.isGioitinh());
                if (nv.isGioitinh() == true) {
                    vector.add("Nam");
                } else {
                    vector.add("Nữ");
                }
                vector.add(nv.getSdtNV());
                vector.add(nv.getMaPB());
                vector.add(nv.getMaCV());
                vector.add(nv.getMaKT());
                dtm.addRow(vector);

            }
            resetForm();
    }

    public boolean checkinfo() {
        Kiemtra c = new Kiemtra();
        if (!c.checkID(jtfMaNV.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập mã sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfMaNV.setText("");
            jtfMaNV.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfHoTen.getText()) || !c.check(jtfHoTen.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfHoTen.setText("");
            jtfHoTen.requestFocus();
            return false;

        } else if (!c.checkSpace(jtfCCCD.getText()) || !c.check(jtfCCCD.getText())) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại nhập CCCD", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfCCCD.setText("");
            jtfCCCD.requestFocus();
            return false;

//        } else if (!c.checkSpace(jtfngaysinh.getText())|| !c.check(jtfHeDaoTao.getText()) || !c.checkDate(jtfngaysinh.getText())) {
//            JOptionPane.showMessageDialog(this, "Kiểm tra lại ngày sinh", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
//            jtfngaysinh.setText("");
//            jtfngaysinh.requestFocus();
//            return false;

        } else if (!c.checkSpace(jtfQueQuan.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập địa chỉ sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfQueQuan.setText("");
            jtfQueQuan.requestFocus();
            return false;

        } else if (!c.checkSpace(jtfDienThoai.getText()) || !c.checkPhone(jtfDienThoai.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập phone sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfDienThoai.setText("");
            jtfDienThoai.requestFocus();
            return false;
        }
        return true;
    }
