    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {

                String MaLuong = jtfMaLuong.getText();
                new LuongDAO().deleteIDLuong(MaLuong);
            } catch (SQLException ex) {
                  JOptionPane.showMessageDialog(this, "Nhân viên này có luong, hãy xóa luong của sinh viên này trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                  JOptionPane.showMessageDialog(this, "Nhân viên này có luong, hãy xóa luong của sinh viên này trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);

            }
            loaddata();
            resetForm();
            jbAdd.setEnabled(true);
            jbUpdate.setEnabled(false);
            jbDelete.setEnabled(false);
            jtfMaLuong.setEnabled(true);
//            jcbMaPB.setEnabled(true);
//        jcbMaCV.setEnabled(true);
//        jcbMaKT.setEnabled(true);
        }
    }//GEN-LAST:event_jbDeleteActionPerformed
