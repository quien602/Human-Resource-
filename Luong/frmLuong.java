    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<Luong> kts = new LuongDAO().getAll();
        for (Luong kt : kts) {
            Vector v = new Vector();
            v.add(kt.getMaLuong());
            v.add(kt.getThangLuong());
            v.add(kt.getNamLuong());
            v.add(kt.getLuongCoBan());
            v.add(kt.getPhuCap());
            v.add(kt.getLuongTangCa());
            v.add(kt.getBHXH());
            v.add(kt.getBHYT());
            v.add(kt.getBHTN());
            v.add(kt.getSoNguoiPT());
            v.add(kt.getThueTNCN());
            v.add(kt.getLuongThucLinh());

            dtm.addRow(v);
        }
        resetForm();
        // jtfthi.setText("");
    }

