    public boolean checkinfo() {
       Kiemtra c = new Kiemtra();
        if (!c.checkID(jtfMaPB.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập mã phòng ban sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfMaPB.setText("");
            jtfMaPB.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfTenPB.getText()) || !c.check(jtfTenPB.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên phòng ban sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfTenPB.setText("");
            jtfTenPB.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfDiaDiemPB.getText()) || !c.check(jtfDiaDiemPB.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập địa điểm sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfTenPB.setText("");
            jtfTenPB.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfSodienthoai.getText()) || !c.checkPhone(jtfSodienthoai.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập phone sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfSodienthoai.setText("");
            jtfSodienthoai.requestFocus();
            return false;
        }
        return true;
    }
