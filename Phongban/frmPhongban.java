    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<Phongban> pbs = new PhongbanDAO().getAll();
        for (Phongban pb : pbs) {
            Vector v = new Vector();
            v.add(pb.getMaPB());
            v.add(pb.getTenPB());
            v.add(pb.getDiaDiemPB());
            v.add(pb.getSdtPB());
            dtm.addRow(v);
        }
        ResetForm();
    }
