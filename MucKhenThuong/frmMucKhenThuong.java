    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
        String MaKT = jtfMaKT.getText();
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                new MucKhenThuongDAO().deleteMucKhenThuong(MaKT);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, có tồn tại điểm của môn này, hãy xóa dữ liệu điểm trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, có tồn tại điểm của môn này, hãy xóa dữ liệu điểm trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            resetForm();
            loaddata();
            jbNew.setEnabled(true);
            jbUpdate.setEnabled(false);
            jbDelete.setEnabled(false);
            jtfMaKT.setEnabled(true);
        }
    }//GEN-LAST:event_jbDeleteActionPerformed
    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<MucKhenThuong> kts = new MucKhenThuongDAO().getAll();
        for (MucKhenThuong kt : kts) {
            Vector v = new Vector();
            v.add(kt.getMaKT());
            v.add(kt.getLyDo());
            v.add(kt.getTienThuong());


            dtm.addRow(v);
        }
        resetForm();
        // jtfthi.setText("");
    }

    public void resetForm() {
        jtfMaKT.setText("");
        jtfLyDo.setText("");
        jtfTienThuong.setText("");
        jtfMaKT.requestFocus();
    }

    public boolean checkinfo() {
        Kiemtra c = new Kiemtra();
        if (!c.checkID(jtfMaKT.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập mã sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfMaKT.setText("");
            jtfMaKT.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfLyDo.getText()) || !c.check(jtfLyDo.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập lý do sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfLyDo.setText("");
            jtfLyDo.requestFocus();
            return false;

        } else if (!c.checkSpace(jtfTienThuong.getText()) || !c.checkNumber(jtfTienThuong.getText())) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfTienThuong.setText("");
            jtfTienThuong.requestFocus();
            return false;
        }
        return true;
    }
