    private void jbNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNewActionPerformed
        String MaKT = jtfMaKT.getText();
        String LyDo = jtfLyDo.getText();
        String TienThuong = jtfTienThuong.getText();

        Kiemtra c = new Kiemtra();
        if (!checkinfo()) {
            return;
        }



        int tt;
        tt = Integer.parseInt(TienThuong);



        MucKhenThuong muckhenThuong = new MucKhenThuong(MaKT, LyDo,tt);
        ArrayList<MucKhenThuong> listCheck = new MucKhenThuongDAO().CheckID(MaKT);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            jtfMaKT.setText("");
            jtfMaKT.requestFocus();
        } else {
            MucKhenThuong insert = new MucKhenThuongDAO().addNew(muckhenThuong);
            if (insert != null) {
                showAll();
            }
        }

    }//GEN-LAST:event_jbNewActionPerformed

