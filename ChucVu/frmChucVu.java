    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        ArrayList<ChucVu> cvs = new ChucVuDAO().getAll();
        for (ChucVu cv : cvs) {
            Vector v = new Vector();
            v.add(cv.getMaCV());
            v.add(cv.getTenCV());
            dtm.addRow(v);
        }
        ResetForm();
    }

    public boolean checkinfo() {
        Kiemtra c = new Kiemtra();
        if (!c.checkID(jtfMaCV.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập Mã chức vụ sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfMaCV.setText("");
            jtfMaCV.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfTenCV.getText()) || !c.check(jtfTenCV.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên chức vụ sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfTenCV.setText("");
            jtfTenCV.requestFocus();
            return false;

        }
        return true;
    }
