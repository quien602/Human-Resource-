    private void jbNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNewActionPerformed
        if (!checkinfo()) {
            return;
        }
        String MaCV = jtfMaCV.getText();
        String TenCV = jtfTenCV.getText();
        ChucVu cv = new ChucVu(MaCV, TenCV);
        ArrayList<ChucVu> listCheck = new ChucVuDAO().checkID(MaCV);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            jtfMaCV.setText("");
            jtfMaCV.requestFocus();
        } else {
            ChucVu insert = new ChucVuDAO().addNew(cv);
            if (insert != null) {
                showAll();
            }
        }


    }//GEN-LAST:event_jbNewActionPerformed

    private void jbUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbUpdateActionPerformed
        String MaCV = jtfMaCV.getText();
        String TenCV = jtfTenCV.getText();
        Kiemtra c = new Kiemtra();
        if (!checkinfo()) {
            return;
        }

        ChucVu cv = new ChucVu(MaCV, TenCV);
        ChucVu update = new ChucVuDAO().updateByID(cv);
        if (update != null) {
            showAll();
        }
        jbNew.setEnabled(true);
        jbUpdate.setEnabled(false);
        jbDelete.setEnabled(false);
        jtfMaCV.setEnabled(true);
    }//GEN-LAST:event_jbUpdateActionPerformed

    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                String MaCV = jtfMaCV.getText();
                new ChucVuDAO().deleteChucVu(MaCV);
            } catch (SQLException ex) {
                Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }

            ResetForm();
            loaddata();
            jbNew.setEnabled(true);
            jbUpdate.setEnabled(false);
            jbDelete.setEnabled(false);
            jtfMaCV.setEnabled(true);
        }
    }//GEN-LAST:event_jbDeleteActionPerformed
